const titles = ["The Engineer", "The Innovator", "The Writer", "The Designer", "The Researcher", "The Creator"];
let currentIndex = 0;

function changeTitle() {
  const titleElement = document.getElementById("title-animate");
  titleElement.textContent = titles[currentIndex];
  currentIndex = (currentIndex + 1) % titles.length;
}

// Change title every 3 seconds to match the CSS animation duration
setInterval(changeTitle, 3000);

// Initialize the first title
changeTitle();

document.querySelector('.contact__button').addEventListener('click', function() {
    const name = document.querySelector('input[type="text"]').value;
    const email = document.querySelector('input[type="mail"]').value;
    const message = document.querySelector('textarea').value;

    fetch('/portfolio--DB.js', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
        },
        body: JSON.stringify({ name, email, message }),
    })
    .then(response => response.text())
    .then(data => {
        alert(data);  // Display success message
    })
    .catch(error => {
        console.error('Error:', error);
    });
});

